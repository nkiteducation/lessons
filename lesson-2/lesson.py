# ================================
# Числа (int, float, complex) в Python
# ================================
# В Python есть три основных числовых типа:
# int      — целые числа (например: -5, 0, 42)
# float    — числа с плавающей точкой (например: 3.14, -0.5, 2.0)
# complex  — комплексные числа (например: 2 + 3j)

# ----------------
# Примеры создания чисел
# ----------------
a = 10            # int
b = -3            # int
c = 3.14          # float
d = -0.5          # float
z = 2 + 3j        # complex

print(a, b, c, d, z)
complex
# ----------------
# Арифметические операции
# ----------------
x = 15
y = 4

print(x + y)    # сложение -> 19
print(x - y)    # вычитание -> 11
print(x * y)    # умножение -> 60
print(x / y)    # деление (всегда float) -> 3.75
print(x // y)   # целочисленное деление -> 3
print(x % y)    # остаток от деления -> 3
print(x ** y)   # возведение в степень -> 50625

# ----------------
# Работа с float
# ----------------
f = 5.6789

print(round(f, 2))   # округление -> 5.68
print(int(f))        # преобразование в int -> 5
print(float(7))      # преобразование в float -> 7.0

# ----------------
# Математические функции (модуль math)
# ----------------
import math

print(math.sqrt(16))    # квадратный корень -> 4.0
print(math.factorial(5))  # факториал 5! = 120
print(math.sin(math.pi / 2))  # синус 90° -> 1.0

# ----------------
# Комплексные числа
# ----------------
z = 3 + 4j
print(z.real)   # действительная часть -> 3.0
print(z.imag)   # мнимая часть -> 4.0
print(abs(z))   # модуль (√(3²+4²)) -> 5.0

# ----------------
# Полезные функции
# ----------------
print(abs(-10))       # модуль числа -> 10
print(pow(2, 3))      # 2**3 -> 8
print(divmod(17, 4))  # (частное, остаток) -> (4, 1)
print(max(1, 7, 3))   # максимум -> 7
print(min(1, 7, 3))   # минимум -> 1
print(sum([1, 2, 3])) # сумма элементов -> 6
