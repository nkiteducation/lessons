# ================================
# Строки (str) в Python
# ================================
# Строка (str) - это упорядоченная последовательность символов.
# Они неизменяемые (immutable), хранят Unicode-символы.

# ----------------
# Как хранится строка
# ----------------
s = "Hello world"
s1 = "Привет"
s2 = """Многострочный
текст"""

# ----------------
# Индексация и срезы
# ----------------
# Доступ по индексу (нумерация с 0)
print(s[6])       # символ по индексу 6 -> "w"

# Срезы (slice)
print(s[:5])      # первые 5 символов -> "Hello"
print(s[6:])      # с 6-го до конца -> "world"
print(s[-11:])    # отрицательный индекс -> "Hello world"
print(s[::2])     # каждый второй символ -> "Hlowrd"

# ----------------
# Операции со строками
# ----------------
# Конкатенация (склеивание)
print("Hello" + " " + "World")

# Умножение строки на число
print("Hi!" * 3)   # Hi!Hi!Hi!

# Проверка вхождения подстроки
print("lo" in "Hello")  # True

# ----------------
# Методы строк
# ----------------
s = "  Python is great!  "

print(s.upper())                       # все буквы в верхний регистр
print(s.lower())                       # все буквы в нижний регистр
print(s.strip())                        # убрать пробелы по краям
print(s.replace("great", "awesome"))    # замена подстроки
print(s.split())                        # разбить по пробелам -> список
print("-".join(["a", "b", "c"]))        # соединение списка через "-"

print(s.startswith("Py"))  # False, строка начинается с пробела
print(s.endswith("!"))     # False, т.к. есть пробел в конце

# ----------------
# Форматирование строк
# ----------------
name = "Alex"
age = 25

# Старый стиль (%)
print("My name is %s, I am %s" % (name, age))

# Метод .format()
print("My name is {}, I am {}".format(name, age))

# f-строки (рекомендуется)
print(f"My name is {name}, I am {age}")

# ----------------
# Полезные функции
# ----------------
print(len("Привет"))  # длина строки -> 6
print(ord("A"))       # код символа -> 65
print(chr(65))        # символ по коду -> "A"
                        